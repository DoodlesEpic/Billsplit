	<h2 style="margin-bottom: 2rem">About Billsplit</h2>
	<p>
		<b>Billsplit</b> is an easy and fast way to split restaurant bills. It's a simple progressive web
		application built with SvelteKit that helps separate costs between people. The application does not
		store your data, nor collect usage analytics.
